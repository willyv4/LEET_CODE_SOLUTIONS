function twoSumII(nums, target) {
  let i = 0;
  let j = nums.length - 1;
  const obj = {};

  while (i < nums.length) {
    if (nums[i] + nums[j] === target) return [i + 1, j + 1];
    const match = target - nums[i];
    const match2 = target - nums[j];

    if (match in obj) {
      return obj[match] < i + 1 ? [obj[match], i + 1] : [i + 1, obj[match]];
    }

    if (match2 in obj) {
      return obj[match2] < j + 1 ? [obj[match2], j + 1] : [j + 1, obj[match2]];
    }

    obj[nums[i]] = i + 1;
    obj[nums[j]] = j + 1;
    i++;
    j--;
  }
  return [];
}

console.log(twoSumII([2, 7, 11, 15], 9)); // [1,2]
console.log(twoSumII([2, 3, 4], 6)); // [1,3]
